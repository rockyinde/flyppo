package com.flyppo.crawler.model;

public class Result {

	String name;
	String url;
	String text;

	public String getName() {
		return name;
	}

	public String getUrl() {
		return url;
	}
	
	public String getText() {
		return text;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setUrl (String url) {
		this.url = url;
	}

	public void setText (String text) {
		this.text = text;
	}
}
